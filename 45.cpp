#include <iostream>

using namespace std;

int main()
{
    int a;

    cout<<"Escribe un numero de 1 a 7:"<<std::endl;
    cin>>a;


    if (a==1){
        cout<<a<<" ---> "<<"Lunes"<<std::endl;
         }

    if (a==2){
        cout<<a<<" ---> "<<"Martes"<<std::endl;
         }

    if (a==3){
        cout<<a<<" ---> "<<"Miercoles"<<std::endl;
         }

    if (a==4){
        cout<<a<<" ---> "<<"Jueves"<<std::endl;
         }

    if (a==5){
        cout<<a<<" ---> "<<"Viernes"<<std::endl;
         }

    if (a==6){
        cout<<a<<" ---> "<<"Sabado"<<std::endl;
         }

    if (a==7){
        cout<<a<<" ---> "<<"Domingo"<<std::endl;
         }

    return 0;
}
